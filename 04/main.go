func main() {
	data := data()
	dataSplit := dataSplit(data)
	count := 0
	for y := 1; y < len(dataSplit)-1; y++ {
		for x := 1; x < len(dataSplit[y])-1; x++ {
			if dataSplit[y][x] == 'A' {
				diag1 := string(dataSplit[y-1][x-1]) + string(dataSplit[y+1][x+1])
				diag2 := string(dataSplit[y-1][x+1]) + string(dataSplit[y+1][x-1])
				if (diag1 == "MS" || diag1 == "SM") && (diag2 == "MS" || diag2 == "SM") {
					count++
				}
			}
		}
	}
	println(count)
}
func part1() {
	data := data()
	dataSplit := dataSplit(data)
	count := 0
	left, downLeft, down, downRight, right, upRight, up, upLeft := 0, 0, 0, 0, 0, 0, 0, 0
	for y, line := range dataSplit {
		for x := range line {
			if line[x] == 'X' {
				// check right
				if x+3 < len(line) {
					if dataSplit[y][x+1] == 'M' && dataSplit[y][x+2] == 'A' && dataSplit[y][x+3] == 'S' {
						right++
						count++
					}
				}
				// check down right
				if x+3 < len(line) && y+3 < len(dataSplit) {
					if dataSplit[y+1][x+1] == 'M' && dataSplit[y+2][x+2] == 'A' && dataSplit[y+3][x+3] == 'S' {
						count++
						downRight++
					}
				}
				// check down
				if y+3 < len(dataSplit) {
					if dataSplit[y+1][x] == 'M' && dataSplit[y+2][x] == 'A' && dataSplit[y+3][x] == 'S' {
						down++
						count++
					}
				}
				// check down left
				if x-3 >= 0 && y+3 < len(dataSplit) {
					if dataSplit[y+1][x-1] == 'M' && dataSplit[y+2][x-2] == 'A' && dataSplit[y+3][x-3] == 'S' {
						downLeft++
						count++
					}
				}
				// check left
				if x-3 >= 0 {

					if dataSplit[y][x-1] == 'M' && dataSplit[y][x-2] == 'A' && dataSplit[y][x-3] == 'S' {
						left++
						count++
					}
				}
				// check up left
				if x-3 >= 0 && y-3 >= 0 {
					if dataSplit[y-1][x-1] == 'M' && dataSplit[y-2][x-2] == 'A' && dataSplit[y-3][x-3] == 'S' {
						upLeft++
						count++
					}
				}
				// check up
				if y-3 >= 0 {
					if dataSplit[y-1][x] == 'M' && dataSplit[y-2][x] == 'A' && dataSplit[y-3][x] == 'S' {
						up++
						count++
					}
				}
				// check up right
				if x+3 < len(line) && y-3 >= 0 {
					if dataSplit[y-1][x+1] == 'M' && dataSplit[y-2][x+2] == 'A' && dataSplit[y-3][x+3] == 'S' {
						upRight++
						count++
					}
				}
			}
		}
	}

	println(count)
}
